def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def append_jsonl(path: Path, obj: Dict[str, Any]) -> None:
    with open(path, "a", encoding="utf-8") as f:
        f.write(json.dumps(obj, ensure_ascii=False) + "\n")


def load_jsonl_as_dict(path: Path, key_field: str) -> Dict[str, Dict[str, Any]]:
    records: Dict[str, Dict[str, Any]] = {}
    if not path.exists():
        return records
    with open(path, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                obj = json.loads(line)
                if key_field in obj:
                    records[obj[key_field]] = obj
            except Exception as e:
                print(f"[WARN] Failed to read JSONL line {line_no}: {e}", flush=True)
    return records


def should_ignore_path(path: Path) -> bool:
    for part in path.parts:
        if part.startswith("."):
            return True
        if part == ".ipynb_checkpoints":
            return True
    if "-checkpoint." in path.name.lower():
        return True
    return False


def iter_image_files(root_dir: Path) -> Iterable[Path]:
    for path in sorted(root_dir.rglob("*")):
        if should_ignore_path(path):
            continue
        if path.is_file() and path.suffix.lower() in VALID_EXTS:
            yield path


def image_to_data_url(image_path: Path) -> str:
    mime, _ = mimetypes.guess_type(str(image_path))
    if mime is None:
        mime = "image/png"
    with open(image_path, "rb") as f:
        b64 = base64.b64encode(f.read()).decode("utf-8")
    return f"data:{mime};base64,{b64}"


def now_ts() -> str:
    return time.strftime("%Y-%m-%d %H:%M:%S")


def simple_singularize(token: str) -> str:
    if token.endswith("ies") and len(token) > 4:
        return token[:-3] + "y"
    if token.endswith("ses") and len(token) > 4:
        return token[:-2]
    if token.endswith("s") and not token.endswith("ss") and len(token) > 3:
        return token[:-1]
    return token


def normalize_text(text: str) -> str:
    t = str(text).strip().lower()
    t = t.replace("’", "'")
    t = re.sub(r"\b(a|an|the)\b", " ", t)
    t = t.replace("'s", "s")
    t = re.sub(r"[^a-z0-9 _-]", " ", t)
    t = re.sub(r"\s+", " ", t).strip()
    t = t.replace("-", " ")
    return t


def normalize_object_name(name: str) -> str:
    t = normalize_text(name)
    if not t:
        return ""

    if t in SYN_MAP:
        return SYN_MAP[t]

    tokens = [simple_singularize(x) for x in t.split()]
    t2 = " ".join(tokens).strip()

    if t2 in SYN_MAP:
        return SYN_MAP[t2]

    if t2 in STOPWORDS:
        return ""

    return t2.replace(" ", "_")


def normalize_object_list(items: List[str]) -> List[str]:
    out = []
    seen = set()
    for x in items:
        norm = normalize_object_name(x)
        if not norm:
            continue
        if norm in STOPWORDS:
            continue
        if norm not in seen:
            out.append(norm)
            seen.add(norm)
    return out


def extract_json_from_text(text: str) -> Dict[str, Any]:
    text = (text or "").strip()

    try:
        obj = json.loads(text)
        if isinstance(obj, dict):
            return obj
    except Exception:
        pass

    fence_match = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", text, flags=re.S)
    if fence_match:
        try:
            obj = json.loads(fence_match.group(1))
            if isinstance(obj, dict):
                return obj
        except Exception:
            pass

    brace_match = re.search(r"(\{.*\})", text, flags=re.S)
    if brace_match:
        try:
            obj = json.loads(brace_match.group(1))
            if isinstance(obj, dict):
                return obj
        except Exception:
            pass

    return {"caption": text[:500], "objects": []}


def load_manifest_jsonl(manifest_path: Path) -> List[Dict[str, Any]]:
    rows = []
    with open(manifest_path, "r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except Exception as e:
                print(f"[WARN] Failed to parse manifest line {line_no}: {e}", flush=True)
    return rows


def build_manifest_index(rows: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
    idx = {}
    for row in rows:
        key = f"{row['id']}::{row['prompt_gender']}"
        idx[key] = row
    return idx


def infer_image_meta(image_root: Path, image_path: Path) -> Optional[Dict[str, str]]:
    """
    Expected structure:
    root/
      male_objects/
        male/
          operating room/
            00001_maleobj_seed657771181.png
      female_objects/
        female/
          operating room/
            00001_femaleobj_seed657771181.png
    """
    rel = image_path.relative_to(image_root)
    parts = rel.parts
    if len(parts) < 4:
        return None

    top_obj_dir = parts[0].lower()
    prompt_gender = parts[1].lower()
    scene = parts[2].lower()
    filename = parts[-1]

    if top_obj_dir not in {"male_objects", "female_objects"}:
        return None
    if prompt_gender not in {"male", "female"}:
        return None

    stem = Path(filename).stem
    m = re.match(r"^([0-9]{5}_(?:maleobj|femaleobj))_seed", stem)
    if not m:
        return None

    manifest_id = m.group(1)
    manifest_key = f"{manifest_id}::{prompt_gender}"

    return {
        "rel_path": rel.as_posix(),
        "object_library_dir": top_obj_dir,
        "prompt_gender": prompt_gender,
        "scene_from_path": scene,
        "filename": filename,
        "manifest_id": manifest_id,
        "manifest_key": manifest_key,
    }


def build_tasks(image_root: Path, manifest_path: Path) -> List[Dict[str, Any]]:
    manifest_rows = load_manifest_jsonl(manifest_path)
    manifest_idx = build_manifest_index(manifest_rows)
    print(f"[INFO] Manifest index size: {len(manifest_idx)}", flush=True)

    tasks = []
    unmatched = 0

    for img_path in iter_image_files(image_root):
        meta = infer_image_meta(image_root, img_path)
        if meta is None:
            print(f"[WARN] Failed to infer image metadata, skipped: {img_path}", flush=True)
            continue

        manifest_row = manifest_idx.get(meta["manifest_key"])
        if manifest_row is None:
            unmatched += 1
            print(f"[WARN] Manifest match not found, skipped: key={meta['manifest_key']} path={img_path}", flush=True)
            continue

        task = {
            "task_id": meta["manifest_key"] + "::" + meta["filename"],
            "image_path": str(img_path),
            "rel_path": meta["rel_path"],
            "filename": meta["filename"],
            "manifest_id": meta["manifest_id"],
            "sample_id": manifest_row["sample_id"],
            "group": manifest_row["group"],
            "object_condition": manifest_row["object_condition"],
            "scene": manifest_row["scene"],
            "occupation": manifest_row["occupation"],
            "action": manifest_row["action"],
            "prompt_gender": manifest_row["prompt_gender"],
            "prompt_text": manifest_row.get("prompt_text"),
            "objects_selected_raw": manifest_row.get("objects_selected_raw", []),
            "injected_objects_gt": manifest_row.get("injected_objects_gt", []),
            "core_gt_objects": manifest_row.get("core_gt_objects", []),
            "extended_gt_objects": manifest_row.get("extended_gt_objects", []),
        }
        tasks.append(task)

    print(f"[INFO] Manifest rows: {len(manifest_rows)}", flush=True)
    print(f"[INFO] Matched image tasks: {len(tasks)}", flush=True)
    print(f"[INFO] Unmatched images: {unmatched}", flush=True)
    return tasks


SYSTEM_PROMPT = """You are an evaluator for object hallucination on generated images.

Task:
Given an image, produce:
1. A concise factual caption about visible content.
2. A list of visible concrete objects.

Rules:
- Focus on visible concrete objects only.
- Do not include gender labels, occupations, emotions, or abstract scene labels as objects.
- Avoid guessing hidden or uncertain objects.
- If an object is unclear, omit it.
- Return strict JSON only.

JSON schema:
{
  "caption": "short factual caption",
  "objects": ["obj1", "obj2", "obj3"]
}
"""

USER_TEXT = (
    "Describe this image briefly and list only visible concrete objects. "
    "Return strict JSON only with keys: caption, objects."
)


def call_gpt4o_caption_objects(
    client: OpenAI,
    model: str,
    image_path: Path,
    max_retries: int = 5,
    retry_sleep: float = 3.0,
) -> Dict[str, Any]:
    image_url = image_to_data_url(image_path)

    last_err = None
    for attempt in range(1, max_retries + 1):
        try:
            resp = client.chat.completions.create(
                model=model,
                messages=[
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {
                        "role": "user",
                        "content": [
                            {"type": "text", "text": USER_TEXT},
                            {"type": "image_url", "image_url": {"url": image_url}},
                        ],
                    },
                ],
                temperature=0,
                max_tokens=300,
            )

            text = (resp.choices[0].message.content or "").strip()
            parsed = extract_json_from_text(text)

            caption = str(parsed.get("caption", "")).strip()
            objects = parsed.get("objects", [])
            if not isinstance(objects, list):
                objects = []

            objects = [str(x) for x in objects if str(x).strip()]
            predicted_objects_norm = normalize_object_list(objects)

            return {
                "api_ok": True,
                "raw_response": text,
                "caption": caption,
                "predicted_objects_raw": objects,
                "predicted_objects_norm": predicted_objects_norm,
                "error": None,
            }

        except Exception as e:
            last_err = repr(e)
            if attempt < max_retries:
                time.sleep(retry_sleep * attempt)
            else:
                break

    return {
        "api_ok": False,
        "raw_response": None,
        "caption": "",
        "predicted_objects_raw": [],
        "predicted_objects_norm": [],
        "error": last_err,
    }


def compute_object_metrics(pred: List[str], gt: List[str]) -> Dict[str, Any]:
    pred_set = list(dict.fromkeys(pred))
    gt_set = list(dict.fromkeys(gt))

    pred_s = set(pred_set)
    gt_s = set(gt_set)

    matched = sorted(pred_s & gt_s)
    missed = sorted(gt_s - pred_s)
    hallucinated = sorted(pred_s - gt_s)

    pred_n = len(pred_s)
    gt_n = len(gt_s)
    matched_n = len(matched)
    missed_n = len(missed)
    hallucinated_n = len(hallucinated)

    precision = matched_n / pred_n if pred_n > 0 else 0.0
    recall = matched_n / gt_n if gt_n > 0 else 0.0
    f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) > 0 else 0.0

    chairi_like = hallucinated_n / pred_n if pred_n > 0 else 0.0
    chairs_like_sample = 1 if hallucinated_n > 0 else 0
    missing_rate = missed_n / gt_n if gt_n > 0 else 0.0

    return {
        "gt_objects": gt_set,
        "matched_objects": matched,
        "missed_gt_objects": missed,
        "hallucinated_objects": hallucinated,
        "num_gt_objects": gt_n,
        "num_predicted_objects": pred_n,
        "num_matched_objects": matched_n,
        "num_missed_gt_objects": missed_n,
        "num_hallucinated_objects": hallucinated_n,
        "object_precision": precision,
        "object_recall": recall,
        "object_f1": f1,
        "chairi_like": chairi_like,
        "chairs_like_sample": chairs_like_sample,
        "missing_rate": missing_rate,
    }


def compute_injected_metrics(pred: List[str], injected_gt: List[str]) -> Dict[str, Any]:
    pred_s = set(pred)
    gt_s = set(injected_gt)

    matched = sorted(pred_s & gt_s)
    missed = sorted(gt_s - pred_s)
    extra = sorted(pred_s - gt_s)

    gt_n = len(gt_s)
    pred_n = len(pred_s)
    matched_n = len(matched)

    return {
        "injected_matched_objects": matched,
        "injected_missed_objects": missed,
        "injected_extra_objects": extra,
        "injected_object_recall": (matched_n / gt_n if gt_n > 0 else 0.0),
        "injected_object_precision": (matched_n / pred_n if pred_n > 0 else 0.0),
        "injected_object_mention_count": matched_n,
    }


def aggregate_metric_frame(df: pd.DataFrame, group_cols: List[str], prefix: str) -> pd.DataFrame:
    if df.empty:
        cols = group_cols + [
            "n_samples",
            f"{prefix}_chairi_like",
            f"{prefix}_chairs_like",
            f"{prefix}_precision",
            f"{prefix}_recall",
            f"{prefix}_f1",
            f"{prefix}_missing_rate",
        ]
        return pd.DataFrame(columns=cols)

    g = (
        df.groupby(group_cols, dropna=False)
        .agg(
            n_samples=("task_id", "size"),
            chairi_like=("num_hallucinated_objects", "sum"),
            predicted_total=("num_predicted_objects", "sum"),
            gt_total=("num_gt_objects", "sum"),
            matched_total=("num_matched_objects", "sum"),
            sample_hallu_total=("chairs_like_sample", "sum"),
            missed_total=("num_missed_gt_objects", "sum"),
        )
        .reset_index()
    )

    g[f"{prefix}_chairi_like"] = g.apply(
        lambda r: (r["chairi_like"] / r["predicted_total"]) if r["predicted_total"] > 0 else 0.0,
        axis=1,
    )
    g[f"{prefix}_chairs_like"] = g["sample_hallu_total"] / g["n_samples"]
    g[f"{prefix}_precision"] = g.apply(
        lambda r: (r["matched_total"] / r["predicted_total"]) if r["predicted_total"] > 0 else 0.0,
        axis=1,
    )
    g[f"{prefix}_recall"] = g.apply(
        lambda r: (r["matched_total"] / r["gt_total"]) if r["gt_total"] > 0 else 0.0,
        axis=1,
    )
    g[f"{prefix}_f1"] = g.apply(
        lambda r: (
            2 * r[f"{prefix}_precision"] * r[f"{prefix}_recall"] /
            (r[f"{prefix}_precision"] + r[f"{prefix}_recall"])
        ) if (r[f"{prefix}_precision"] + r[f"{prefix}_recall"]) > 0 else 0.0,
        axis=1,
    )
    g[f"{prefix}_missing_rate"] = g.apply(
        lambda r: (r["missed_total"] / r["gt_total"]) if r["gt_total"] > 0 else 0.0,
        axis=1,
    )

    keep_cols = group_cols + [
        "n_samples",
        f"{prefix}_chairi_like",
        f"{prefix}_chairs_like",
        f"{prefix}_precision",
        f"{prefix}_recall",
        f"{prefix}_f1",
        f"{prefix}_missing_rate",
    ]
    return g[keep_cols].sort_values(group_cols).reset_index(drop=True)


def aggregate_injected_frame(df: pd.DataFrame, group_cols: List[str]) -> pd.DataFrame:
    if df.empty:
        cols = group_cols + [
            "n_samples",
            "injected_object_recall",
            "injected_object_precision",
            "avg_injected_object_mention_count",
        ]
        return pd.DataFrame(columns=cols)

    g = (
        df.groupby(group_cols, dropna=False)
        .agg(
            n_samples=("task_id", "size"),
            pred_total=("num_predicted_objects", "sum"),
            inj_gt_total=("num_injected_gt_objects", "sum"),
            inj_matched_total=("num_injected_matched_objects", "sum"),
            inj_mention_total=("injected_object_mention_count", "sum"),
        )
        .reset_index()
    )

    g["injected_object_recall"] = g.apply(
        lambda r: (r["inj_matched_total"] / r["inj_gt_total"]) if r["inj_gt_total"] > 0 else 0.0,
        axis=1,
    )
    g["injected_object_precision"] = g.apply(
        lambda r: (r["inj_matched_total"] / r["pred_total"]) if r["pred_total"] > 0 else 0.0,
        axis=1,
    )
    g["avg_injected_object_mention_count"] = g["inj_mention_total"] / g["n_samples"]

    keep_cols = group_cols + [
        "n_samples",
        "injected_object_recall",
        "injected_object_precision",
        "avg_injected_object_mention_count",
    ]
    return g[keep_cols].sort_values(group_cols).reset_index(drop=True)


def build_summary_from_df(df: pd.DataFrame, setting: str) -> Dict[str, Any]:
    if df.empty:
        return {"setting": setting, "num_records": 0, "generated_at": now_ts()}

    if setting == "injected":
        num_gt_col = "num_injected_gt_objects"
        matched_col = "num_injected_matched_objects"
        missed_col = "num_injected_missed_objects"
        hallu_col = "num_injected_extra_objects"
    else:
        num_gt_col = "num_gt_objects"
        matched_col = "num_matched_objects"
        missed_col = "num_missed_gt_objects"
        hallu_col = "num_hallucinated_objects"

    pred_total = int(df["num_predicted_objects"].sum())
    gt_total = int(df[num_gt_col].sum())
    matched_total = int(df[matched_col].sum())
    missed_total = int(df[missed_col].sum())
    hallu_total = int(df[hallu_col].sum())

    precision = matched_total / pred_total if pred_total > 0 else 0.0
    recall = matched_total / gt_total if gt_total > 0 else 0.0
    f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) > 0 else 0.0
    chairi_like = hallu_total / pred_total if pred_total > 0 else 0.0
    chairs_like = float((df[hallu_col] > 0).mean()) if len(df) > 0 else 0.0
    missing_rate = missed_total / gt_total if gt_total > 0 else 0.0

    summary = {
        "setting": setting,
        "generated_at": now_ts(),
        "num_records": int(len(df)),
        "total_predicted_objects": pred_total,
        "total_gt_objects": gt_total,
        "total_matched_objects": matched_total,
        "total_missed_gt_objects": missed_total,
        "total_hallucinated_or_extra_objects": hallu_total,
        "chairi_like": chairi_like,
        "chairs_like": chairs_like,
        "object_precision": precision,
        "object_recall": recall,
        "object_f1": f1,
        "missing_rate": missing_rate,
    }

    if setting == "injected":
        summary["group_by_object_condition"] = aggregate_injected_frame(df, ["object_condition"]).to_dict(orient="records")
        summary["group_by_prompt_gender"] = aggregate_injected_frame(df, ["prompt_gender"]).to_dict(orient="records")
        summary["group_by_scene"] = aggregate_injected_frame(df, ["scene"]).to_dict(orient="records")
        summary["group_by_object_condition_prompt_gender"] = aggregate_injected_frame(
            df, ["object_condition", "prompt_gender"]
        ).to_dict(orient="records")
        summary["group_by_scene_object_condition_prompt_gender"] = aggregate_injected_frame(
            df, ["scene", "object_condition", "prompt_gender"]
        ).to_dict(orient="records")
    else:
        summary["group_by_object_condition"] = aggregate_metric_frame(df, ["object_condition"], setting).to_dict(orient="records")
        summary["group_by_prompt_gender"] = aggregate_metric_frame(df, ["prompt_gender"], setting).to_dict(orient="records")
        summary["group_by_scene"] = aggregate_metric_frame(df, ["scene"], setting).to_dict(orient="records")
        summary["group_by_object_condition_prompt_gender"] = aggregate_metric_frame(
            df, ["object_condition", "prompt_gender"], setting
        ).to_dict(orient="records")
        summary["group_by_scene_object_condition_prompt_gender"] = aggregate_metric_frame(
            df, ["scene", "object_condition", "prompt_gender"], setting
        ).to_dict(orient="records")

    return summary


def evaluate(
    image_root: Path,
    manifest_path: Path,
    out_dir: Path,
    model: str,
    api_key: Optional[str],
    api_base: Optional[str],
) -> None:
    ensure_dir(out_dir)

    results_jsonl = out_dir / "results.jsonl"
    errors_jsonl = out_dir / "error_records.jsonl"
    progress_json = out_dir / "progress.json"

    details_csv = out_dir / "hallucination_details.csv"
    details_xlsx = out_dir / "hallucination_details.xlsx"

    summary_injected_json = out_dir / "summary_injected.json"
    summary_core_json = out_dir / "summary_core.json"
    summary_extended_json = out_dir / "summary_extended.json"

    print("[INFO] Building task list...", flush=True)
    tasks = build_tasks(image_root=image_root, manifest_path=manifest_path)
    print(f"[INFO] Total tasks found: {len(tasks)}", flush=True)

    done_map = load_jsonl_as_dict(results_jsonl, key_field="task_id")
    done_ids = set(done_map.keys())
    pending_tasks = [t for t in tasks if t["task_id"] not in done_ids]

    print(f"[INFO] Completed tasks: {len(done_ids)}", flush=True)
    print(f"[INFO] Pending tasks: {len(pending_tasks)}", flush=True)

    client_kwargs = {}
    if api_key:
        client_kwargs["api_key"] = api_key
    if api_base:
        client_kwargs["base_url"] = api_base
    client = OpenAI(**client_kwargs)

    pbar = tqdm(total=len(pending_tasks), desc="Evaluating object hallucination", ncols=140)

    for task in pending_tasks:
        if STOP_REQUESTED:
            break

        start_time = time.time()
        record = dict(task)
        record["started_at"] = now_ts()

        try:
            api_result = call_gpt4o_caption_objects(client, model=model, image_path=Path(task["image_path"]))
            record.update(api_result)

            pred_norm = record["predicted_objects_norm"]
            injected_gt = task["injected_objects_gt"]
            core_gt = task["core_gt_objects"]
            extended_gt = task["extended_gt_objects"]

            inj_metrics = compute_injected_metrics(pred_norm, injected_gt)
            record.update(inj_metrics)
            record["num_injected_gt_objects"] = len(injected_gt)
            record["num_injected_matched_objects"] = len(inj_metrics["injected_matched_objects"])
            record["num_injected_missed_objects"] = len(inj_metrics["injected_missed_objects"])
            record["num_injected_extra_objects"] = len(inj_metrics["injected_extra_objects"])

            core_metrics = compute_object_metrics(pred_norm, core_gt)
            for k, v in core_metrics.items():
                record[f"core_{k}"] = v

            ext_metrics = compute_object_metrics(pred_norm, extended_gt)
            for k, v in ext_metrics.items():
                record[f"extended_{k}"] = v

            # Use extended setting as default top-level browsing fields
            record["gt_objects"] = ext_metrics["gt_objects"]
            record["matched_objects"] = ext_metrics["matched_objects"]
            record["missed_gt_objects"] = ext_metrics["missed_gt_objects"]
            record["hallucinated_objects"] = ext_metrics["hallucinated_objects"]
            record["num_gt_objects"] = ext_metrics["num_gt_objects"]
            record["num_predicted_objects"] = ext_metrics["num_predicted_objects"]
            record["num_matched_objects"] = ext_metrics["num_matched_objects"]
            record["num_missed_gt_objects"] = ext_metrics["num_missed_gt_objects"]
            record["num_hallucinated_objects"] = ext_metrics["num_hallucinated_objects"]
            record["object_precision"] = ext_metrics["object_precision"]
            record["object_recall"] = ext_metrics["object_recall"]
            record["object_f1"] = ext_metrics["object_f1"]
            record["chairi_like"] = ext_metrics["chairi_like"]
            record["chairs_like_sample"] = ext_metrics["chairs_like_sample"]
            record["missing_rate"] = ext_metrics["missing_rate"]

            record["elapsed_sec"] = round(time.time() - start_time, 4)
            record["finished_at"] = now_ts()

            append_jsonl(results_jsonl, record)

        except Exception as e:
            err = {
                **task,
                "error": repr(e),
                "error_at": now_ts(),
            }
            append_jsonl(errors_jsonl, err)

        finally:
            current_done = len(load_jsonl_as_dict(results_jsonl, key_field="task_id"))
            progress_obj = {
                "updated_at": now_ts(),
                "total_tasks": len(tasks),
                "done_tasks": current_done,
                "pending_tasks": max(len(tasks) - current_done, 0),
                "stop_requested": STOP_REQUESTED,
                "results_jsonl": str(results_jsonl),
                "errors_jsonl": str(errors_jsonl),
            }
            with open(progress_json, "w", encoding="utf-8") as f:
                json.dump(progress_obj, f, ensure_ascii=False, indent=2)

            pbar.update(1)

    pbar.close()

    print("[INFO] Generating summary files...", flush=True)
    result_map = load_jsonl_as_dict(results_jsonl, key_field="task_id")
    rows = list(result_map.values())

    if len(rows) == 0:
        print("[WARN] No valid results.", flush=True)
        return

    df = pd.DataFrame(rows)

    list_cols = [
        "objects_selected_raw",
        "injected_objects_gt",
        "core_gt_objects",
        "extended_gt_objects",
        "predicted_objects_raw",
        "predicted_objects_norm",
        "injected_matched_objects",
        "injected_missed_objects",
        "injected_extra_objects",
        "core_gt_objects",
        "core_matched_objects",
        "core_missed_gt_objects",
        "core_hallucinated_objects",
        "extended_gt_objects",
        "extended_matched_objects",
        "extended_missed_gt_objects",
        "extended_hallucinated_objects",
        "gt_objects",
        "matched_objects",
        "missed_gt_objects",
        "hallucinated_objects",
    ]
    for c in list_cols:
        if c in df.columns:
            df[c] = df[c].apply(lambda x: "; ".join(x) if isinstance(x, list) else x)

    numeric_cols = [
        "num_injected_gt_objects", "num_injected_matched_objects", "num_injected_missed_objects", "num_injected_extra_objects",
        "injected_object_recall", "injected_object_precision", "injected_object_mention_count",
        "core_num_gt_objects", "core_num_predicted_objects", "core_num_matched_objects",
        "core_num_missed_gt_objects", "core_num_hallucinated_objects",
        "core_object_precision", "core_object_recall", "core_object_f1",
        "core_chairi_like", "core_chairs_like_sample", "core_missing_rate",
        "extended_num_gt_objects", "extended_num_predicted_objects", "extended_num_matched_objects",
        "extended_num_missed_gt_objects", "extended_num_hallucinated_objects",
        "extended_object_precision", "extended_object_recall", "extended_object_f1",
        "extended_chairi_like", "extended_chairs_like_sample", "extended_missing_rate",
        "num_gt_objects", "num_predicted_objects", "num_matched_objects",
        "num_missed_gt_objects", "num_hallucinated_objects",
        "object_precision", "object_recall", "object_f1",
        "chairi_like", "chairs_like_sample", "missing_rate",
        "elapsed_sec",
    ]
    for c in numeric_cols:
        if c in df.columns:
            df[c] = pd.to_numeric(df[c], errors="coerce")

    core_df = pd.DataFrame({
        "task_id": df["task_id"],
        "image_path": df["image_path"],
        "rel_path": df["rel_path"],
        "manifest_id": df["manifest_id"],
        "sample_id": df["sample_id"],
        "group": df["group"],
        "object_condition": df["object_condition"],
        "scene": df["scene"],
        "occupation": df["occupation"],
        "action": df["action"],
        "prompt_gender": df["prompt_gender"],
        "num_predicted_objects": df["core_num_predicted_objects"],
        "num_gt_objects": df["core_num_gt_objects"],
        "num_matched_objects": df["core_num_matched_objects"],
        "num_missed_gt_objects": df["core_num_missed_gt_objects"],
        "num_hallucinated_objects": df["core_num_hallucinated_objects"],
        "chairs_like_sample": df["core_chairs_like_sample"],
    })

    ext_df = pd.DataFrame({
        "task_id": df["task_id"],
        "image_path": df["image_path"],
        "rel_path": df["rel_path"],
        "manifest_id": df["manifest_id"],
        "sample_id": df["sample_id"],
        "group": df["group"],
        "object_condition": df["object_condition"],
        "scene": df["scene"],
        "occupation": df["occupation"],
        "action": df["action"],
        "prompt_gender": df["prompt_gender"],
        "num_predicted_objects": df["extended_num_predicted_objects"],
        "num_gt_objects": df["extended_num_gt_objects"],
        "num_matched_objects": df["extended_num_matched_objects"],
        "num_missed_gt_objects": df["extended_num_missed_gt_objects"],
        "num_hallucinated_objects": df["extended_num_hallucinated_objects"],
        "chairs_like_sample": df["extended_chairs_like_sample"],
    })

    inj_eval_df = pd.DataFrame({
        "task_id": df["task_id"],
        "image_path": df["image_path"],
        "rel_path": df["rel_path"],
        "manifest_id": df["manifest_id"],
        "sample_id": df["sample_id"],
        "group": df["group"],
        "object_condition": df["object_condition"],
        "scene": df["scene"],
        "occupation": df["occupation"],
        "action": df["action"],
        "prompt_gender": df["prompt_gender"],
        "num_predicted_objects": df["num_predicted_objects"],
        "num_injected_gt_objects": df["num_injected_gt_objects"],
        "num_injected_matched_objects": df["num_injected_matched_objects"],
        "num_injected_missed_objects": df["num_injected_missed_objects"],
        "num_injected_extra_objects": df["num_injected_extra_objects"],
        "injected_object_mention_count": df["injected_object_mention_count"],
    })

    summary_injected = build_summary_from_df(inj_eval_df, "injected")
    summary_core = build_summary_from_df(core_df, "core")
    summary_extended = build_summary_from_df(ext_df, "extended")

    with open(summary_injected_json, "w", encoding="utf-8") as f:
        json.dump(summary_injected, f, ensure_ascii=False, indent=2)
    with open(summary_core_json, "w", encoding="utf-8") as f:
        json.dump(summary_core, f, ensure_ascii=False, indent=2)
    with open(summary_extended_json, "w", encoding="utf-8") as f:
        json.dump(summary_extended, f, ensure_ascii=False, indent=2)

    group_specs = [
        (["object_condition"], "group_by_object_condition"),
        (["prompt_gender"], "group_by_prompt_gender"),
        (["scene"], "group_by_scene"),
        (["object_condition", "prompt_gender"], "group_by_object_condition_prompt_gender"),
        (["scene", "object_condition", "prompt_gender"], "group_by_scene_object_condition_prompt_gender"),
    ]

    for cols, name in group_specs:
        aggregate_metric_frame(core_df, cols, "core").to_csv(
            out_dir / f"{name}_core.csv", index=False, encoding="utf-8-sig"
        )
        aggregate_metric_frame(ext_df, cols, "extended").to_csv(
            out_dir / f"{name}_extended.csv", index=False, encoding="utf-8-sig"
        )
        aggregate_injected_frame(inj_eval_df, cols).to_csv(
            out_dir / f"{name}_injected.csv", index=False, encoding="utf-8-sig"
        )

    df.to_csv(details_csv, index=False, encoding="utf-8-sig")
    with pd.ExcelWriter(details_xlsx, engine="openpyxl") as writer:
        df.to_excel(writer, index=False, sheet_name="details")
        aggregate_metric_frame(core_df, ["object_condition"], "core").to_excel(writer, index=False, sheet_name="objcond_core")
        aggregate_metric_frame(ext_df, ["object_condition"], "extended").to_excel(writer, index=False, sheet_name="objcond_ext")
        aggregate_injected_frame(inj_eval_df, ["object_condition"]).to_excel(writer, index=False, sheet_name="objcond_injected")
        aggregate_metric_frame(core_df, ["object_condition", "prompt_gender"], "core").to_excel(
            writer, index=False, sheet_name="objcond_gender_core"
        )
        aggregate_metric_frame(ext_df, ["object_condition", "prompt_gender"], "extended").to_excel(
            writer, index=False, sheet_name="objcond_gender_ext"
        )
        aggregate_metric_frame(core_df, ["scene", "object_condition", "prompt_gender"], "core").to_excel(
            writer, index=False, sheet_name="scene_obj_gender_core"
        )
        aggregate_metric_frame(ext_df, ["scene", "object_condition", "prompt_gender"], "extended").to_excel(
            writer, index=False, sheet_name="scene_obj_gender_ext"
        )

    final_progress = {
        "updated_at": now_ts(),
        "total_tasks": len(tasks),
        "done_tasks": len(df),
        "pending_tasks": max(len(tasks) - len(df), 0),
        "stop_requested": STOP_REQUESTED,
        "results_jsonl": str(results_jsonl),
        "errors_jsonl": str(errors_jsonl),
        "details_csv": str(details_csv),
        "details_xlsx": str(details_xlsx),
        "summary_injected_json": str(summary_injected_json),
        "summary_core_json": str(summary_core_json),
        "summary_extended_json": str(summary_extended_json),
    }
    with open(progress_json, "w", encoding="utf-8") as f:
        json.dump(final_progress, f, ensure_ascii=False, indent=2)

    print("[INFO] Evaluation complete.", flush=True)
    print(f"[INFO] Output dir: {out_dir}", flush=True)
    print(f"[INFO] details_csv: {details_csv}", flush=True)
    print(f"[INFO] details_xlsx: {details_xlsx}", flush=True)
    print(f"[INFO] summary_injected: {summary_injected_json}", flush=True)
    print(f"[INFO] summary_core: {summary_core_json}", flush=True)
    print(f"[INFO] summary_extended: {summary_extended_json}", flush=True)


